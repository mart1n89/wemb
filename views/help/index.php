<h2>H&auml;ufig gestellte Fragen</h2>
<div id="textBlock">
    
    <p><b>F&uuml;r wen ist Testimeter nutzbar?</b></p>
    <p>Testimeter ist f&uuml;r alle Angeh&ouml;rigen der Hochschule Flensburg nach Beantragung eines Nutzeraccounts beim Administrator (aktuell Herr Prof. M&uuml;ller) zug&auml;nglich.</p>
    <p><b>Wie bekomme ich einen Account?</b></p>
    <p>Die Beantragung kann formlos per Mail an mueller@hs-flensburg.de erfolgen. Studierende f&uuml;gen dem Antrag bitte eine Immatrikulationsbescheinigung bei.</p>
    <p><b>Was bringt Testimeter der Hochschule Flensburg?</b></p>
    <p>
       Nutzen f&uuml;r die Hochschule besteht in der M&ouml;glichkeit, den Lehrenden aufzeigen zu k&ouml;nnen, ob die vermittelten Inhalte korrekt von den Studierenden 
       verstanden wurden und diese in der Lage sind, entsprechende Fragen zum Themengebiet zu beantworten. Aus der Beantwortung der Fragen k&ouml;nnen die Lehrenden 
       erkennen, ob andere Herangehensweisen oder auch ggfs. vertiefende Informationen zu Darlegung der vermittelten Sachverhalte notwendig erscheinen.
    </p>
    <p><b>Wie konfiguriere ich den Timer richtig?</b></p>
    <p>
        Jeder Nutzer hat einen individuell konfigurierbaren User-Timer. Zus&auml;tzlich besteht die M&ouml;glichkeit, pro Fragerunde einen sog. Session-Timer zu setzen, 
        der nur f&uuml;r die Fragerunde besteht. Sollten Sie keinen Session-Timer vergeben, so w&auml;hlt das System Ihren User-Timer f&uuml;r die Fragerunde aus.
    </p>
    <p><b>Wie ist Testimeter erreichbar?</b></p>
    <p>Testimeter ist im WLAN eduroam der Hochschule Flensburg und ausserhalb der Hochschule &uuml;ber VPN zug&auml;nglich. Ein externer Zugriff ist nicht m&ouml;glich.</p>
</div>
